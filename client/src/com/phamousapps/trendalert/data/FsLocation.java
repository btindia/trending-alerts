package com.phamousapps.trendalert.data;

public class FsLocation {

	String address;
	String crossStreet;
	double lat;
	double lon;
	long distance;
	String postalCode;
	String city;
	String state;
	String country;
	String cc;

	public String getAddress() {
		return address;
	}

	public String getCrossStreet() {
		return crossStreet;
	}

	public double getLat() {
		return lat;
	}

	public double getLon() {
		return lon;
	}

	public long getDistance() {
		return distance;
	}

	public String getPostalCode() {
		return postalCode;
	}

	public String getCity() {
		return city;
	}

	public String getState() {
		return state;
	}

	public String getCountry() {
		return country;
	}

	public String getCc() {
		return cc;
	}

}
